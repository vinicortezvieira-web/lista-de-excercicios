let num1 = 10;
let num2 = 5;

let resultado = num1 + num2;
console.log(`a soma entre 10 e 5 é ${resultado}`);

let resultado2 = num1 - num2;
console.log(`a subtração entre 10 e 5 é ${resultado2}`);

let resultado3 = num1 * num2;
console.log(`a multiplicação entre 10 e 5 é de ${resultado3}`);

let resultado4 = num1 / num2;
console.log(`a divisão de 10 e 5 é de ${resultado4}`);

// Em JS, potência usa ** em vez de ^
let resultado5 = num1 ** num2; 
console.log(`a potenciação de 10 e 5 é de ${resultado5}`);